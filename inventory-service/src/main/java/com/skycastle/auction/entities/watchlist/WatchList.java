package com.skycastle.auction.entities.watchlist;

import com.skycastle.auction.entities.BaseEntity;
import com.skycastle.auction.entities.products.Product;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.OneToMany;
import jakarta.persistence.Table;
import lombok.Data;
import lombok.EqualsAndHashCode;

import java.util.ArrayList;
import java.util.List;

@EqualsAndHashCode(callSuper = true)
@Data
@Entity
@Table(name="watch_list")
public class WatchList extends BaseEntity {

    @Column(name = "buyer_id", nullable = false, unique = true) // only allow a buyer to have just one watch list
    private Long buyerId;

    @OneToMany
    private List<Product> products =  new ArrayList<>();
}