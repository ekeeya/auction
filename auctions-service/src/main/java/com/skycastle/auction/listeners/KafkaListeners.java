package com.skycastle.auction.listeners;

import com.skycastle.auction.dtos.kafka.MessageDTO;
import lombok.RequiredArgsConstructor;
import org.springframework.kafka.annotation.KafkaListener;
import org.springframework.messaging.simp.SimpMessagingTemplate;
import org.springframework.stereotype.Component;

@Component
@RequiredArgsConstructor
public class KafkaListeners {

    private final SimpMessagingTemplate simpMessagingTemplate;

    @KafkaListener(topics="auction-session", groupId ="groupId")
    void listener(MessageDTO data){

        simpMessagingTemplate.convertAndSend("/topic/bids", data);
    }
}
